% 数据
x = [0 1 2 3 4]';      % 自变量列向量
y = [1.2 2.8 7.1 20.1 54.8]';  % 因变量列向量

% 构建设计矩阵
% 每一行对应一个数据点，每一列对应一个参数
% 第一列是 x，第二列是 1（对应截距 b）
A = [x, ones(length(x),1)];

% 求最小二乘解
% MATLAB 的 "\" 操作符会求出最优参数，使得 y ≈ A * [a; b] 的误差平方和最小
params = A \ y;

% 拆开参数
a = params(1);  % 斜率
b = params(2);  % 截距

% 输出结果
disp(['a = ', num2str(a), ', b = ', num2str(b)])
